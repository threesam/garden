// Bakes the deana photos into ASCII *character matrices* (not rasterized
// pixels) shipped as a TS module the homepage gallery card consumes
// directly. Replaces the heavy webp pre-bakes for that card while leaving
// the runtime AsciiImage path on /deana untouched.
//
// Output: src/lib/deana/ascii-matrices.ts (a TS module). Tones for fill-
// opacity are derived at render time from each glyph's index in the RAMP
// — no second matrix needed.
//
// Run: pnpm bake:deana-matrix

import sharp from "sharp";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join, parse } from "node:path";

const SRC_DIR = "static/assets";
const SRC_FILES = [
  "deana-6.webp",
  "deana-5.webp",
  "deana-hero-3.webp",
  "deana-hero.webp",
  "deana-hero-5.webp",
  "deana-hero-6.webp",
];
const OUT = "src/lib/deana/ascii-matrices.ts";

// Fixed matrix dimensions, baked to match the homepage card's 4:5 aspect.
// Uniform across all photos so crossfades don't shift the cell grid (the
// renderer's cellW/cellH stay constant regardless of which matrix is in
// view). Source photos are cover-cropped to fit this aspect at bake time.
//
// HEIGHT_RATIO 1.8 means each glyph cell is 1.8× as tall as wide. For a
// 4:5 card (h/w = 1.25): rows / cols = (h/w) / HEIGHT_RATIO = 1.25 / 1.8
// ≈ 0.694. With COLS = 76 that gives ROWS ≈ 52.8 → 53. Bumped from
// 54×38 → finer grid means smaller chars on screen.
const COLS = 76;
const ROWS = 53;
const HEIGHT_RATIO = 1.8; // documented; not used in the bake itself

// Identical to scripts/generate-deana-ascii.mjs so the chosen glyphs
// match what the existing pre-bake produces — flipping consumer paths
// shouldn't change the look meaningfully.
const RAMP =
  " `.-':_,^=;><+!rc*/z?sLTv)J7(|Fi{C}fI31tlu[neoZ5Yxjya]2ESwqkP6h9d4VpOGbUAKXHm8RD#$Bg0MNWQ%&@";
const clamp = (v, lo, hi) => Math.max(lo, Math.min(hi, v));
function lumToTone(lum) {
  const stretched = clamp((lum - 0.1) / 0.65, 0, 1);
  const s2 = stretched * stretched * (3 - 2 * stretched);
  return 1 - s2 * s2 * (3 - 2 * s2);
}

async function bake(src) {
  // Two-stage sample. The renderer draws each glyph in a cell that is
  // HEIGHT_RATIO (1.8) times as tall as wide, so the matrix grid's pixel
  // aspect (COLS:ROWS = 1.43:1) maps to a DISPLAY aspect of 4:5. Sharp
  // doesn't know about the cell aspect, so a direct cover-fit to COLS x
  // ROWS would crop a 1.43:1 landscape band from the source — which then
  // gets vertically stretched at render time. To avoid that:
  //
  // 1. Extract a 4:5 (DISPLAY) crop of the source, centred. That's what
  //    will actually be visible on the card.
  // 2. Squish that crop down to COLS x ROWS with `fill`. The squish is
  //    exactly the inverse of what the renderer's tall cells uncompress.
  const meta = await sharp(src).metadata();
  const srcW = meta.width ?? 0;
  const srcH = meta.height ?? 0;
  const TARGET_ASPECT = COLS / (ROWS * HEIGHT_RATIO); // = 4/5 by construction
  const srcAspect = srcW / srcH;
  let extractW;
  let extractH;
  if (srcAspect > TARGET_ASPECT) {
    // source is wider than 4:5 — crop sides
    extractH = srcH;
    extractW = Math.round(extractH * TARGET_ASPECT);
  } else {
    // source is taller than 4:5 — crop top/bottom
    extractW = srcW;
    extractH = Math.round(extractW / TARGET_ASPECT);
  }
  const extractLeft = Math.round((srcW - extractW) / 2);
  const extractTop = Math.round((srcH - extractH) / 2);

  const { data } = await sharp(src)
    .extract({ left: extractLeft, top: extractTop, width: extractW, height: extractH })
    .resize(COLS, ROWS, { fit: "fill" })
    .toColourspace("srgb")
    .removeAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });

  const lines = [];
  for (let y = 0; y < ROWS; y++) {
    let line = "";
    for (let x = 0; x < COLS; x++) {
      const off = (y * COLS + x) * 3;
      const lum = (0.2126 * data[off] + 0.7152 * data[off + 1] + 0.0722 * data[off + 2]) / 255;
      const tone = lumToTone(lum);
      line += RAMP[Math.floor(tone * (RAMP.length - 1))] ?? " ";
    }
    lines.push(line);
  }
  return { cols: COLS, rows: ROWS, chars: lines.join("\n") };
}

const matrices = {};
let totalChars = 0;
for (const f of SRC_FILES) {
  const base = parse(f).name;
  matrices[base] = await bake(join(SRC_DIR, f));
  totalChars += matrices[base].chars.length;
  console.log(
    `${base}: ${matrices[base].cols}x${matrices[base].rows} = ${matrices[base].chars.length} chars`,
  );
}

const moduleSource = `// Generated by scripts/generate-deana-ascii-matrix.mjs — do not edit.
//
// ASCII character matrices for the deana homepage card. Replaces the heavy
// webp pre-bakes (~2 MB total) with a few KB of char data the browser
// rasterises live. The RAMP used at bake time matches AsciiImage's, so
// fill-opacity can be derived per cell from each glyph's RAMP index.

export const ASCII_RAMP =
  ${JSON.stringify(RAMP)};

export interface AsciiMatrix {
  cols: number;
  rows: number;
  /** One glyph per cell, rows separated by '\\n'. */
  chars: string;
}

export const DEANA_MATRICES: AsciiMatrix[] = [
${Object.entries(matrices)
  .map(([_k, v]) => `  ${JSON.stringify(v)},`)
  .join("\n")}
];
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, moduleSource);

const moduleBytes = Buffer.byteLength(moduleSource);
console.log(
  `\n${totalChars} total cells across ${SRC_FILES.length} matrices. Wrote ${OUT} (${moduleBytes} bytes).`,
);
